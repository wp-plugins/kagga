ಕೇಳಿಯುಂ ಮಾಯೆಯದು; ನೃತ್ಯಗತಿ ಬರಿತೋರ್ಕೆ ।<br />ಮೂಲಮಂ ಕಂಡಂಗೆ ಸಂಭ್ರಾಂತಿಯೇನು? ॥<br />ಆಳದಲಿ ನಿರ್ಲಿಪ್ತ ಮೇಲೆ ನಿಯತಿಕ್ಲುಪ್ತ ।<br />ಲೀಲಾಪ್ರಿಯಂ ಬ್ರಹ್ಮ – ಮಂಕುತಿಮ್ಮ ॥ ೯೪ ॥
ಶ್ಯಾಮಸುಂದರನವನೆ ಚಕ್ರಿ ನರಹರಿಯಂತೆ ।<br />ಸೋಮಶಂಕರನೆ ಭೈರವ ರುದ್ರನಂತೆ ॥<br />ಹೈಮವತಿ ಶಿವೆ ತಾನೆ ಕಾಳಿ ಚಂಡಿಕೆಯಂತೆ ।<br />ಪ್ರೇಮ ಘೋರಗಳೊಂದೆ! – ಮಂಕುತಿಮ್ಮ ॥ ೯೫ ॥
